    PRESERVE8
    THUMB
    AREA     max_of_3, CODE, READONLY
    EXPORT __main
	ENTRY	
__main  FUNCTION
		MOV R2, #32
		MOV R3, #64
		MOV R4, #80
		CMP R2,R3
		BGT GTCALL
		BLE LECALL
STOP B STOP 
		
GTCALL	CMP R2,R4
		MOVGT R7,R2
		MOVLE R7,R4
		B STOP
		
LECALL  CMP R3,R4
		MOVGT R7,R3
		MOVLE R7,R4
		B STOP
	ENDFUNC
	END
