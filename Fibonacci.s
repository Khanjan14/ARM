   PRESERVE8
    THUMB
    AREA     Fibonacci, CODE, READONLY
    EXPORT __main
	ENTRY	
__main  FUNCTION
		MOV R2,#9		;nTH ELEMENT OF FIBONAKI SERIES
		MOV R3,#0		;STARTING FROM 0,1
		MOV R4,#1		;CURRENT ELEMENT(E2)
		SUBS R2,#2		;AS FIRST AND SECOND ELEMENTS ARE FIXED
LOOP	MOV R5,R4
		ADDS R4,R3,R4
		SUBS R2,#1
		MOV R3,R5
		CMP R2,#0
		BNE LOOP
		
STOP B STOP 
	ENDFUNC
	END
